public class task5_14 {

    public static void tableAdd() {
        int i = 0;
        System.out.println("Table of Addition by 9: ");
        for (i = 1; i <= 9; i++) {
            System.out.println(i * 9);
        }
    }

    public static void main(String[] args) {
        tableAdd();
    }
}
